package operadores;

public class Operadores3 {
    public static void main(String[] args) {
        int numero = 5;
        //i repetição
        numero = numero + 2;
        System.out.println(numero);

        numero=5;
        numero = numero + 10;
        System.out.println(numero);

        numero=5;
        numero++;
        System.out.println(numero);

        boolean variavel = true;

        System.out.println(variavel);

        variavel = !variavel;
        System.out.println(variavel);
    }
}
